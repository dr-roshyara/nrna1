import ElectionLayout from "@/Layouts/ElectionLayout";

export default {
    components: {
        ElectionLayout,
    },
    
    mounted() {
        // Add keyboard navigation support
        this.setupKeyboardNavigation();
        
        // Announce page load to screen readers
        this.announcePageLoad();
    },
    
    methods: {
        setupKeyboardNavigation() {
            // Add keyboard event listeners for better accessibility
            document.addEventListener('keydown', (e) => {
                // Add custom keyboard shortcuts if needed
                if (e.altKey && e.key === 'v') {
                    // Alt+V for voting (accessibility shortcut)
                    window.location.href = 'code/create';
                }
                if (e.altKey && e.key === 'r') {
                    // Alt+R for results
                    window.location.href = 'election/result';
                }
            });
        },
        
        announcePageLoad() {
            // Create an announcement for screen readers
            const announcement = document.createElement('div');
            announcement.setAttribute('aria-live', 'polite');
            announcement.setAttribute('aria-atomic', 'true');
            announcement.className = 'sr-only';
            announcement.textContent = 'निर्वाचन केन्द्र लोड भयो। Election Center loaded.';
            document.body.appendChild(announcement);
            
            // Remove after announcement
            setTimeout(() => {
                document.body.removeChild(announcement);
            }, 1000);
        }
    }
};
</script>

